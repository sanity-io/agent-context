import {defineType} from 'sanity'

function createSchema(name: string) {
  return defineType({
    name,
    title: name.charAt(0).toUpperCase() + name.slice(1),
    type: 'document',
    fields: [
      {
        name: 'name',
        title: 'Name',
        type: 'string',
      },
    ],
  })
}

